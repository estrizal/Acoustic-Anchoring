"""
Script: 09_verify_qwen_confidence_epochs_wer.py

Evaluates the Qwen LoRA models trained WITH inline acoustic confidence scores.
This is the confidence-score analogue of:

    reproducible/06_verify_qwen_epochs_wer.py

Run from project root:
    python reproducible/09_verify_qwen_confidence_epochs_wer.py

Useful smoke test:
    python reproducible/09_verify_qwen_confidence_epochs_wer.py --max-samples 50

Files needed:
    models/phonemic_char_ipa_FULL_best.pt        - acoustic model used to create confidence data
    data/char_ipa_vocab.json                     - char-level IPA vocabulary
    models/lora_yes_confidence_models/checkpoint-4197/  - confidence LoRA Epoch 1
    models/lora_yes_confidence_models/checkpoint-8394/  - confidence LoRA Epoch 2
    models/lora_yes_confidence_models/checkpoint-12591/ - confidence LoRA Epoch 3
    data/LibriSpeech/test-clean/                 - downloaded automatically if absent

Dataset lineage:
    data/qwen_finetune_confidence.jsonl was generated by
    scripts/training/generate_qwen_confidence_dataset.py. That script uses the
    char-IPA unfrozen acoustic model and prefixes each decoded phonetic word
    with a confidence score, e.g. "[0.94] dhax [0.31] ...".
"""

import argparse
import csv
import glob
import json
import math
import os
import re
import sys

import jiwer
import numpy as np
import soundfile as sf
import torch
import torch.nn as nn
import whisper as openai_whisper
from peft import PeftModel
from tqdm import tqdm
from transformers import AutoModelForCausalLM, AutoTokenizer

sys.path.insert(0, os.path.abspath(os.path.join(os.path.dirname(os.path.abspath(__file__)), "..")))


CTC_WEIGHTS = "models/phonemic_char_ipa_FULL_best.pt"
VOCAB_PATH = "data/char_ipa_vocab.json"
DATA_ROOT = "./data"
LIBRI_ROOT = "./data/LibriSpeech/test-clean"
QWEN_MODEL = "Qwen/Qwen2.5-0.5B-Instruct"
OUTPUT_CSV = "results/qwen_confidence_epoch_wer.csv"

SYSTEM_PROMPT = (
    "You are an expert phonetic decoder. Your task is to decode noisy IPA "
    "phonetic transcripts into standard English. To help you, each word's "
    "phonetic transcription is preceded by a confidence score in brackets, "
    "like [0.94]."
)

CHECKPOINTS = {
    "Ep 3 (ckpt-12591)": "models/lora_yes_confidence_models/checkpoint-12591",
}

NO_CONFIDENCE_BASELINE = {
    "Ep 1": 17.65,
    "Ep 2": 16.65,
    "Ep 3": 16.30,
}


class CharIPATokenizer:
    def __init__(self):
        self.blank_id = 0
        self.pad_id = 1
        self.unk_id = 2
        self.char2id = {"<BLANK>": 0, "<PAD>": 1, "<UNK>": 2}
        self.id2char = {0: "<BLANK>", 1: "<PAD>", 2: "<UNK>"}
        self.vocab_size = 3

    def load(self, path):
        with open(path, "r", encoding="utf-8") as f:
            data = json.load(f)
        self.char2id = data["char2id"]
        self.id2char = {int(v): k for k, v in self.char2id.items()}
        self.vocab_size = len(self.char2id)


class ConvCTCAdapter(nn.Module):
    def __init__(self, d_model, vocab_size, dropout=0.1):
        super().__init__()
        self.ln1 = nn.LayerNorm(d_model)
        self.conv1 = nn.Conv1d(d_model, d_model, kernel_size=5, padding=2)
        self.act1 = nn.GELU()
        self.drop1 = nn.Dropout(dropout)
        self.ln2 = nn.LayerNorm(d_model)
        self.conv2 = nn.Conv1d(d_model, d_model, kernel_size=5, padding=2)
        self.act2 = nn.GELU()
        self.drop2 = nn.Dropout(dropout)
        self.ln3 = nn.LayerNorm(d_model)
        self.linear = nn.Linear(d_model, vocab_size)

    def forward(self, x):
        x = self.ln1(x).transpose(1, 2)
        x = self.drop1(self.act1(self.conv1(x))).transpose(1, 2)
        x = self.ln2(x).transpose(1, 2)
        x = self.drop2(self.act2(self.conv2(x))).transpose(1, 2)
        return self.linear(self.ln3(x))


class WhisperForPhonemicCTC(nn.Module):
    def __init__(self, vocab_size, dropout=0.1):
        super().__init__()
        full = openai_whisper.load_model("tiny")
        self.encoder = full.encoder
        self.dims = full.dims
        self.ctc_head = ConvCTCAdapter(self.dims.n_audio_state, vocab_size, dropout=dropout)
        del full

    def forward(self, mel):
        return self.ctc_head(self.encoder(mel))


def load_pairs(root):
    pairs = []
    for trans_file in glob.glob(os.path.join(root, "**", "*.trans.txt"), recursive=True):
        folder = os.path.dirname(trans_file)
        with open(trans_file, encoding="utf-8") as f:
            for line in f:
                line = line.strip()
                if not line:
                    continue
                uid, *parts = line.split()
                flac = os.path.join(folder, uid + ".flac")
                if os.path.exists(flac):
                    pairs.append((flac, " ".join(parts).lower()))
    return pairs


def norm(text):
    return re.sub(
        r"\s+",
        " ",
        text.lower().translate(str.maketrans("", "", ".,?!;:\"'")),
    ).strip()


def bootstrap_ci(errors, lengths, n_boot=1000, seed=42):
    rng = np.random.default_rng(seed)
    n = len(errors)
    wers = []
    for _ in range(n_boot):
        idx = rng.choice(n, n, replace=True)
        e = np.sum(errors[idx])
        l = np.sum(lengths[idx])
        wers.append(e / l if l > 0 else 0)
    wers.sort()
    return wers[25] * 100, wers[975] * 100


def confidence_prompt_from_logits(logits, tokenizer, log_v):
    probs = torch.softmax(logits, dim=-1)
    token_ids = torch.argmax(probs, dim=-1).tolist()
    token_tensor = torch.tensor(token_ids, device=logits.device, dtype=torch.long)

    entropy = -(probs * torch.log(probs + 1e-9)).sum(dim=-1)
    p_drawn = probs[torch.arange(len(token_ids), device=logits.device), token_tensor]
    composite = p_drawn * (1.0 - entropy / log_v)

    decoded = []
    word_confs = []
    current_confs = []
    prev = None
    space_id = tokenizer.char2id.get("<SPACE>")

    for t, tid in enumerate(token_ids):
        if tid == tokenizer.blank_id:
            prev = tid
            continue
        if tid == prev:
            continue
        if tid == space_id:
            if current_confs:
                word_confs.append(float(np.mean(current_confs)))
                current_confs = []
            decoded.append(" ")
        else:
            char = tokenizer.id2char.get(tid, "")
            if char:
                decoded.append(char)
                current_confs.append(float(composite[t]))
        prev = tid

    if current_confs:
        word_confs.append(float(np.mean(current_confs)))

    ipa_str = "".join(decoded).strip()
    words = ipa_str.split()
    while len(word_confs) < len(words):
        word_confs.append(0.50)
    return " ".join(f"[{conf:.2f}] {word}" for word, conf in zip(words, word_confs))


def build_confidence_dataset(pairs, ctc_model, ipa_tokenizer, device):
    log_v = math.log(ipa_tokenizer.vocab_size)
    dataset = []
    for audio_path, ref in tqdm(pairs, desc="CTC confidence decoding"):
        audio, sr = sf.read(audio_path, dtype="float32")
        if audio.ndim > 1:
            audio = audio.mean(axis=1)
        true_audio_len_s = len(audio) / 16000.0
        audio = openai_whisper.pad_or_trim(audio.astype(np.float32))
        mel = openai_whisper.log_mel_spectrogram(audio).unsqueeze(0).to(device)
        with torch.no_grad():
            logits = ctc_model(mel)[0, : int(true_audio_len_s * 50), :]
        dataset.append((ref, confidence_prompt_from_logits(logits, ipa_tokenizer, log_v)))
    return dataset


def load_base_model():
    if torch.cuda.is_available():
        return AutoModelForCausalLM.from_pretrained(
            QWEN_MODEL,
            torch_dtype=torch.float16,
            device_map="cuda:0",
            attn_implementation="sdpa",
        )
    return AutoModelForCausalLM.from_pretrained(QWEN_MODEL)


def evaluate_checkpoint(ckpt_path, tokenizer, confidence_dataset, batch_size):
    # PEFT mutates the supplied base model; use a clean base per checkpoint so
    # epoch comparisons do not accidentally stack adapters.
    base_model = load_base_model()
    llm = PeftModel.from_pretrained(base_model, ckpt_path)
    llm.eval()

    errors, lengths = [], []
    refs = [ref for ref, _ in confidence_dataset]
    prompts_in = [prompt for _, prompt in confidence_dataset]

    for start in tqdm(range(0, len(confidence_dataset), batch_size), desc=os.path.basename(ckpt_path)):
        batch_prompts = prompts_in[start : start + batch_size]
        batch_refs = refs[start : start + batch_size]
        prompts = [
            f"<|im_start|>system\n{SYSTEM_PROMPT}<|im_end|>\n"
            f"<|im_start|>user\n{prompt}<|im_end|>\n"
            f"<|im_start|>assistant\n"
            for prompt in batch_prompts
        ]
        inputs = tokenizer(prompts, return_tensors="pt", padding=True).to(llm.device)
        with torch.no_grad():
            out = llm.generate(
                **inputs,
                max_new_tokens=256,
                pad_token_id=tokenizer.pad_token_id,
                eos_token_id=tokenizer.eos_token_id,
            )
        for j, ref in enumerate(batch_refs):
            in_len = inputs.input_ids[j].size(0)
            hyp = norm(tokenizer.decode(out[j][in_len:], skip_special_tokens=True))
            result = jiwer.process_words(norm(ref), hyp)
            errors.append(result.substitutions + result.deletions + result.insertions)
            lengths.append(len(norm(ref).split()))

    errors = np.array(errors)
    lengths = np.array(lengths)
    wer = np.sum(errors) / np.sum(lengths) * 100
    ci_lo, ci_hi = bootstrap_ci(errors, lengths)
    del llm
    if torch.cuda.is_available():
        torch.cuda.empty_cache()
    return wer, ci_lo, ci_hi


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--max-samples", type=int, default=None, help="Limit test-clean utterances for a smoke test.")
    parser.add_argument("--batch-size", type=int, default=16, help="Qwen generation batch size.")
    parser.add_argument("--output-csv", default=OUTPUT_CSV)
    args = parser.parse_args()

    for path in [CTC_WEIGHTS, VOCAB_PATH]:
        if not os.path.exists(path):
            print(f"[ERROR] Missing: {path}")
            sys.exit(1)
    for ckpt in CHECKPOINTS.values():
        if not os.path.exists(ckpt):
            print(f"[ERROR] Missing checkpoint: {ckpt}")
            sys.exit(1)

    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    print("=" * 72)
    print("Qwen WITH confidence-score LoRA evaluation")
    print(f"Device: {device}")
    print(f"Acoustic model: {CTC_WEIGHTS}")
    print("=" * 72)

    import torchaudio

    torchaudio.datasets.LIBRISPEECH(root=DATA_ROOT, url="test-clean", download=True)
    pairs = load_pairs(LIBRI_ROOT)
    if args.max_samples is not None:
        pairs = pairs[: args.max_samples]
    print(f"Loaded {len(pairs)} test-clean pairs.")

    ipa_tokenizer = CharIPATokenizer()
    ipa_tokenizer.load(VOCAB_PATH)
    ctc_model = WhisperForPhonemicCTC(ipa_tokenizer.vocab_size).to(device)
    ctc_model.load_state_dict(torch.load(CTC_WEIGHTS, map_location=device, weights_only=False))
    ctc_model.eval()

    confidence_dataset = build_confidence_dataset(pairs, ctc_model, ipa_tokenizer, device)
    del ctc_model
    if torch.cuda.is_available():
        torch.cuda.empty_cache()

    tokenizer = AutoTokenizer.from_pretrained(QWEN_MODEL)
    tokenizer.padding_side = "left"
    if tokenizer.pad_token_id is None:
        tokenizer.pad_token = tokenizer.eos_token

    rows = []
    print("\n" + "-" * 72)
    for label, ckpt in CHECKPOINTS.items():
        epoch_label = label.split()[0] + " " + label.split()[1]
        print(f"\nEvaluating {label}: {ckpt}")
        wer, ci_lo, ci_hi = evaluate_checkpoint(ckpt, tokenizer, confidence_dataset, args.batch_size)
        no_conf = NO_CONFIDENCE_BASELINE[epoch_label]
        delta = wer - no_conf
        rows.append(
            {
                "epoch": epoch_label,
                "confidence_checkpoint": ckpt,
                "n": len(confidence_dataset),
                "confidence_wer": wer,
                "confidence_ci_low": ci_lo,
                "confidence_ci_high": ci_hi,
                "no_confidence_wer_from_06": no_conf,
                "delta_conf_minus_no_conf": delta,
            }
        )
        print(f"  WITH confidence: {wer:.2f}%  CI [{ci_lo:.2f}, {ci_hi:.2f}]")
        print(f"  NO confidence baseline from 06: {no_conf:.2f}%")
        print(f"  Delta (confidence - no confidence): {delta:+.2f} pp")

    os.makedirs(os.path.dirname(args.output_csv), exist_ok=True)
    with open(args.output_csv, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=list(rows[0].keys()))
        writer.writeheader()
        writer.writerows(rows)

    print("\n" + "=" * 72)
    print("Side-by-side summary")
    print("Epoch | No-confidence WER | With-confidence WER | Delta")
    for row in rows:
        print(
            f"{row['epoch']:>5} | {row['no_confidence_wer_from_06']:.2f}%"
            f" | {row['confidence_wer']:.2f}% [{row['confidence_ci_low']:.2f}, {row['confidence_ci_high']:.2f}]"
            f" | {row['delta_conf_minus_no_conf']:+.2f} pp"
        )
    print(f"\nSaved CSV: {args.output_csv}")


if __name__ == "__main__":
    main()
